    function autoSlide(type) {
      if (type === 'desktop') {
        showSlides(slideIndexDesktop, 'desktop');
        slideIndexDesktop++;
        setTimeout(() => autoSlide('desktop'), 5000); // Change slide every 2 seconds
      } else {
        showSlides(slideIndexMobile, 'mobile');
